package customListExample;

public class Candy {

	private String color; // RED, YELLOW, BLUE
	private String flavor; // GINGER, FRUITY, HONEY, BUTTER
	private String shape; // CIRCLE, RECTANGLE
	
	public String getColor() {
		return color;
	}
	
	public void setColor(String color) {
		this.color = color;
	}
	
	public String getFlavor() {
		return flavor;
	}
	
	public void setFlavor(String flavor) {
		this.flavor = flavor;
	}
	
	public String getShape() {
		return shape;
	}
	
	public void setShape(String shape) {
		this.shape = shape;
	}
}
